
using System.Collections.Generic;


public static class Shuffle
{
    public static System.Random rng = new System.Random();
    public static void WorkEvents<T>(this IList<T> list)
    {
        int n = list.Count;
        while (n > 1)
        {
            n--;
            int k = rng.Next(n + 1);
            T value = list[k];
            list[k] = list[n];
            list[n] = value;
        }
    }
}
